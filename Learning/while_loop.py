#python while loop
#in python we use while loop to repeat a block of code untill certain condition is met
#The while loop evaluates condition, which is a boolean expression. if the condition is true,body of while loop is executed.The condition is evaluated again.This process continues untill the condition is false.
#once condition is evaluate false the loop terminates

Num=1
while Num<=10:
    print(Num)
    Num=Num+1
